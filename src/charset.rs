pub fn get_random_char() -> char {
    let mut char_table: Vec<char> = ('!'..='~').collect(); // Includes almost whole range of ASCII printable characters (33 to 126)
    char_table.extend_from_slice(&[
        'ç', 'Ç', 'ü', 'Ü', 'é', 'É', 'â', 'Â', 'ä', 'Ä', 'à', 'À', 'å', 'Å', 'ê', 'Ê', 'ë', 'Ë',
        'è', 'È', 'ï', 'Ï', 'î', 'Î', 'ì', 'Ì', 'æ', 'Æ', 'ô', 'Ô', 'ö', 'Ö', 'ò', 'Ò', 'û', 'Û',
        'ù', 'Ù', 'ÿ', '¢', '£', '¥', 'ƒ', 'Ƒ', 'á', 'Á', 'í', 'Í', 'ó', 'Ó', 'ú', 'Ú', 'ñ', 'Ñ',
        'ª', 'º', '¿', '¬', '½', '¼', '¡', '«', '»', 'α', 'ß', 'Γ', 'π', 'Σ', 'σ', 'µ', 'τ', 'Φ',
        'Θ', 'Ω', 'δ', 'φ', 'ε', '±', '÷', '°', '·', '²', '¶', '⌐', '₧', '░', '▒', '▓', '│', '┤',
        '╡', '╢', '╕', '╣', '║', '╗', '╝', '╜', '╛', '┐', '└', '┴', '┬', '├', '─', '┼', '╞', '╟',
        '╚', '╔', '╩', '╦', '╠', '═', '╬', '╧', '╨', '╤', '╧', '╙', '╘', '╒', '╓', '╪', '┘', '┌',
        '█', '▄', '▌', '▐', '▀', '∞', '∩', '≡', '≥', '≤', '⌠', '⌡', '≈', '∙', '√', 'ⁿ', '■',
    ]);

    return char_table[fastrand::usize(..char_table.len())];
}
